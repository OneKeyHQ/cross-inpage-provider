#!/usr/bin/env node

/**
 * Post-build script: generates desktop-specific injection outputs from the
 * webpack-built injectedDesktop.js.
 *
 * Outputs (all written to dist/injected/):
 *   - injectedDesktopCode.js
 *       The provider code wrapped with a require("electron") shim that maps
 *       ipcRenderer to window.__onekeyDesktopBridge. Ready to be passed to
 *       webFrame.executeJavaScript() or loaded as a text module.
 *
 *   - injectedDesktopPreload.js
 *       Complete Electron <webview> preload script (contextIsolation: true,
 *       sandbox: true). Exposes an IPC bridge via contextBridge, then injects
 *       the provider into the page's main world via webFrame.executeJavaScript.
 */

const fs = require('fs');
const path = require('path');

const distDir = path.resolve(__dirname, '..', 'dist', 'injected');
const injectedDesktopPath = path.join(distDir, 'injectedDesktop.js');

if (!fs.existsSync(injectedDesktopPath)) {
  console.error(
    'ERROR: dist/injected/injectedDesktop.js not found. Run webpack first.',
  );
  process.exit(1);
}

const injectedCode = fs.readFileSync(injectedDesktopPath, 'utf-8');

// require("electron") shim: maps to window.__onekeyDesktopBridge exposed by contextBridge
const requireShim = [
  'var require=function(m){',
  'if(m==="electron"){return{ipcRenderer:{',
  'on:function(ch,cb){window.__onekeyDesktopBridge.onHostMessage(function(d){cb({},d);});},',
  'sendToHost:function(ch,d){window.__onekeyDesktopBridge.sendToHost(ch,d);}',
  '}};}',
  'throw new Error("Cannot require "+m);',
  '};',
].join('');

const wrappedProviderCode = `(function(){${requireShim}\n${injectedCode}})();`;

// 1. injectedDesktopCode.js — provider + require shim, for executeJavaScript
fs.writeFileSync(
  path.join(distDir, 'injectedDesktopCode.js'),
  wrappedProviderCode,
);
console.log('Generated dist/injected/injectedDesktopCode.js');

// 2. injectedDesktopPreload.js — complete webview preload script
const preloadContent = `/* eslint-disable */
/**
 * AUTO-GENERATED by scripts/generate-desktop-outputs.js — do not edit manually.
 *
 * Webview preload script (contextIsolation: true, sandbox: true).
 * 1. Exposes a minimal IPC bridge via contextBridge.
 * 2. Injects the Web3 provider (injectedDesktop.js) into the page's main world
 *    via webFrame.executeJavaScript (DOM is not available at preload time).
 */
(function () {
var electron = require('electron');
var contextBridge = electron.contextBridge;
var ipcRenderer = electron.ipcRenderer;
var webFrame = electron.webFrame;

var HOST_CHANNEL = 'JsBridgeDesktopHostToInjected';

// --- IPC bridge (accessible from page world as window.__onekeyDesktopBridge) ---
contextBridge.exposeInMainWorld('__onekeyDesktopBridge', {
  sendToHost: function (channel, data) {
    ipcRenderer.sendToHost(channel, data);
  },
  onHostMessage: function (callback) {
    ipcRenderer.on(HOST_CHANNEL, function (_event, data) {
      callback(data);
    });
  },
});

// --- Inject provider into page main world ---
// webFrame.executeJavaScript runs code in the main world's V8 context.
// Called from preload, it executes before the page's own scripts.
webFrame.executeJavaScript(${JSON.stringify(wrappedProviderCode)}).catch(function (err) {
  console.error('[OneKey] Failed to inject provider', err);
});
})();
`;

fs.writeFileSync(
  path.join(distDir, 'injectedDesktopPreload.js'),
  preloadContent,
);
console.log('Generated dist/injected/injectedDesktopPreload.js');
