type ITreeNodeInfo = {
  isLeaf: boolean;
  isOpen: boolean;
  isClosed: boolean;
  isInternal: boolean;
  data: ITreeNodeData;
  toggle: () => void;
};
type ITreeNodeData = {
  id: string;
  name: string;
  icon?: string;
  href?: string;
  target?: string;
  children?: ITreeNodeData[];
};

type ITreeNodeProps = {
  node: ITreeNodeInfo;
  style: unknown;
  dragHandle: RefObject<HTMLDivElement>;
};
function TreeNode({ node, style, dragHandle }: ITreeNodeProps) {
  const { href, target, name, icon } = node.data;

  const link = useMemo(() => {
    if (href) {
      if (target) {
        return (
          <a href={href} target={target} className="text-black font-normal hover:underline">
            {`${name} â†’`}
          </a>
        );
      } else {
        return (
          <Link href={href} className="text-black font-normal hover:underline">{`${name} â†’`}</Link>
        );
      }
    }

    return <span className="text-lg font-medium">{name}</span>;
  }, [name, target, href]);

  const imgIcon = useMemo(() => {
    if (icon) {
      return <img alt={icon} src={icon} className="w-6 h-6 rounded-full" />;
    }
    if (node.isInternal) {
      if (node.isClosed) {
        return 'ğŸ“';
      }
      return 'ğŸ“‚';
    }
    return 'ğŸ‘‰';
  }, [icon, node.isClosed, node.isInternal]);

  return (
    <div
      style={style}
      ref={dragHandle}
      onClick={() => node.toggle()}
      className="flex flex-row items-center cursor-pointer"
    >
      <div className="flex flex-row items-center min-w-4 mr-2">{imgIcon}</div>
      {link}
    </div>
  );
}

